OUTPUT_FILE = "../DatasetK.csv"
DELIMITER = "|"


# --- ФУНКЦИЯ ДЛЯ ОЧИСТКИ ---
def clean_text(text):
    text = text.strip()
    text = re.sub(r"\s+", " ", text)
    return text


# --- ПАРСИНГ ---
rows = []

